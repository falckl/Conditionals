package com.qa.day4;

public class Bird extends Animal {
	
	private boolean fly;
	private String feathers;
	
	public boolean getFly() {
		return fly;
	}
	
	public void setFly(boolean canFly) {
		this.fly = canFly;
	}
	
	public String getFeathers() {
		return feathers;
	}
	
	public void setFeathers(boolean canFly) {
		this.fly = canFly;
	}
	

}
